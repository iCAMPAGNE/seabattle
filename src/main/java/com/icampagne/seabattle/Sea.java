package com.icampagne.seabattle;

public class Sea {
	
	private int h;
	private int v;
	private int status;

	public int getH() {
		return h;
	}
	public void setH(int h) {
		this.h = h;
	}
	public int getV() {
		return v;
	}
	public void setV(int v) {
		this.v = v;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
}
